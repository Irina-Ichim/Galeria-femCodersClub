  <h1>Política de Privacidad y Normas de Uso de la Galería Pública de femCoders Club</h1>
  
  <ul>
    <li><strong>Introducción</strong></li>
    <p>¡Bienvenidas a la Galería Pública de femCoders Club! Al registrarse y utilizar nuestra plataforma, usted acepta cumplir con las siguientes normas y políticas. Estas normas están diseñadas para garantizar un entorno seguro, respetuoso y agradable para toda la comunidad de femCoders Club.</p>
  
    <li><strong>Registro de Usuarios</strong></li>
    <p>Para ser parte de nuestra comunidad y poder subir fotos, es necesario registrarse como usuaria. Al registrarse, usted confirma que:</p>
    <ul>
      <li>Es mayor de 18 años.</li>
      <li>Proporcionará información veraz y actualizada.</li>
      <li>Forma parte de la comunidad femenina de programadoras.</li>
    </ul>
  
    <li><strong>Normas de Publicación de Contenidos</strong></li>
    <p>Las usuarias registradas pueden subir fotos de eventos organizados por femCoders Club. Para garantizar una experiencia positiva para todas, se deben seguir estas normas:</p>
    <ul>
      <li>Contenido permitido:
        <ul>
          <li>Solo se permiten fotos de eventos realizados por femCoders Club.</li>
          <li>Las fotos deben mostrar la esencia del evento, destacando la participación de las mujeres en el ámbito de la programación.</li>
          <li>Se pueden incluir fotos de las ponentes, asistentes, talleres, actividades, etc.</li>
        </ul>
      </li>
      <li>Prohibiciones:
        <ul>
          <li>No se permiten fotos que incluyan menores de edad sin el consentimiento expreso de sus padres o tutores legales.</li>
          <li>No se permiten fotos que contengan contenido ofensivo, inapropiado o que vulnere los derechos de otras personas.</li>
          <li>No se permiten fotos que promuevan la discriminación, el odio o la violencia.</li>
          <li>No se permiten fotos que sean irrelevantes para la comunidad de femCoders Club.</li>
        </ul>
      </li>
    </ul>
  
    <li><strong>Protección de la Privacidad</strong></li>
    <p>Respetamos su privacidad y nos comprometemos a proteger sus datos personales.</p>
  
    <li><strong>Consecuencias del Incumplimiento</strong></li>
    <p>El incumplimiento de estas normas resultará en acciones disciplinarias que pueden incluir:</p>
    <ul>
      <li>Eliminación de las fotos que infrinjan las normas.</li>
      <li>Suspensión o eliminación de la cuenta de la usuaria infractora.</li>
      <li>Otras medidas que femCoders Club considere necesarias.</li>
    </ul>
  
    <li><strong>Decisiones de femCoders Club</strong></li>
    <p>femCoders Club se reserva el derecho de tomar decisiones finales sobre cualquier contenido subido a la galería y sobre las acciones disciplinarias correspondientes en caso de incumplimiento de estas normas.</p>
  
    <li><strong>Contacto</strong></li>
    <p>Si tiene alguna pregunta o inquietud sobre esta política, puede contactarnos a través de nuestro correo electrónico: <a href="mailto:info@femcodersclub.com">info@femcodersclub.com</a>.</p>
  </ul>
  